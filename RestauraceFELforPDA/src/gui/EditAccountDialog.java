    private void accountBoxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_accountBoxItemStateChanged
        try {
            int accountId = accountBox.getSelectedIndex();
            String table = null, person = null, discount = null;
            Object[][] accounts = ServiceFacade.getInstance().getAccounts();
            String name = accounts[accountId][1].toString();
            try {
                table = accounts[accountId][2].toString();
                person = accounts[accountId][3].toString();
                discount = accounts[accountId][4].toString();
            } catch (NullPointerException ex) {

            }
            accountNameField.setText(name);
            if (table == null) tableField.setText("");
            else tableField.setText(table);
            if (person == null) personField.setText("");
            else personField.setText(person);
            if (discount == null) discountField.setText("");
            else discountField.setText(discount);
            
        } catch (Exception ex) {

        }
    }//GEN-LAST:event_accountBoxItemStateChanged

