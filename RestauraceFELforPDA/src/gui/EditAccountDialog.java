    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
       int i = isValidInput();
        if (i == 1) {
            JOptionPane.showMessageDialog(this, "Nazev uctu \nmusi byt \nvyplnen.", "Chyba", JOptionPane.ERROR_MESSAGE);
            return;
        }
        else {
                try {
                Object [][] accounts = ServiceFacade.getInstance().getAccounts();
                int accId = Integer.parseInt(accounts[accountBox.getSelectedIndex()][0].toString());
                String accName = accountNameField.getText();
                int stulId = 0;
                if (!tableField.getText().equals("")) stulId = Integer.parseInt(tableField.getText());
                int statusId = 2;
                String user = personField.getText();
                int userId = 0;
                if (!user.equals("")) {
                    userId = ServiceFacade.getInstance().getUserByUsername(user).getUserId();
                }
                String discountType = discountField.getText();
                int discountTypeId = 0;
                if (!discountType.equals("")) {
                    discountTypeId = ServiceFacade.getInstance().getDiscountTypeByName(discountType).getDiscountTypeId();
                }
                boolean isOK = ServiceFacade.getInstance().updateAccount(accId, accName, statusId, stulId, userId, discountTypeId);
                if (!isOK) {
                    JOptionPane.showMessageDialog(this, "Ucet s timto \nnazvem jiz \nexistuje.", "Účet", JOptionPane.INFORMATION_MESSAGE);
                    return;
                }
                else this.dispose();
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_updateButtonActionPerformed

    protected void refresh() throws FileNotFoundException, NotBoundException, RemoteException {
        int accountId = 1;
        String table = null, person = null, discount = null;
        Object[][] accounts = ServiceFacade.getInstance().getAccounts();
        String name = accounts[accountId][1].toString();
        try {
            table = accounts[accountId][2].toString();
            person = accounts[accountId][3].toString();
            discount = accounts[accountId][4].toString();
        } catch (NullPointerException ex) {
            
        }
        accountNameField.setText(name);
        if (table == null) tableField.setText("");
        else tableField.setText(table);
        if (person == null) personField.setText("");
        else personField.setText(person);
        if (discount == null) discountField.setText("");
        else discountField.setText(discount);
        String [] accNames = ServiceFacade.getInstance().getAccountNames();
        setComboBoxModel(accNames, accountBox);
    }

